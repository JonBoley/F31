	+ 90*1*(strcmp(char(file1(1,13:15)),'Apr'))...
	+ 120*1*(strcmp(char(file1(1,13:15)),'May'))...
	+ 151*1*(strcmp(char(file1(1,13:15)),'Jun'))...
	+ 181*1*(strcmp(char(file1(1,13:15)),'Jul'))...
	+ 212*1*(strcmp(char(file1(1,13:15)),'Aug'))...
	+ 243*1*(strcmp(char(file1(1,13:15)),'Sep'))...
	+ 273*1*(strcmp(char(file1(1,13:15)),'Oct'))...
	+ 304*1*(strcmp(char(file1(1,13:15)),'Nov'))...
	+ 334*1*(strcmp(char(file1(1,13:15)),'Dec'))...
	+ str2num(char(file1(1,11:12)));

